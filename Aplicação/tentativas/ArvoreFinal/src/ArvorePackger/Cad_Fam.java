    public void Cadastrando(){
        
        User p = new User();
        User_DAO dao = new User_DAO();

        p.setLogin(txtLogin.getText());
        p.setSenha(txtSenha.getText());
        
        dao.create(p);

        txtLogin.setText("");
        txtSenha.setText("");
        
        
        if((txtLogin!=null) &&(txtSenha!=null) && (txtSenhaAut!=null)){
            if ((txtSenha==txtSenhaAut)){
            new jMenu().setVisible(true);
            this.setVisible(false);
            }else{
            JOptionPane.showMessageDialog(null,"As senhas n√£o coicidem");
            }
        }else if((txtLogin==null)||(txtSenha==null)){
            JOptionPane.showMessageDialog(null,"Preencha todos os campos");
        }
    }

